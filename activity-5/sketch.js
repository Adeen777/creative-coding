function setup() {
  createCanvas(400, 400);
  background(20);
  noStroke();
}

function draw() {

  let x = random(width);
  let y = random(height);
  let size = random(10, 50);

  fill(random(100, 255), random(100, 255), random(100, 255), 100);
  ellipse(x, y, size, size);

  fill(20, 20, 20, 5);
  rect(0, 0, width, height);
}
